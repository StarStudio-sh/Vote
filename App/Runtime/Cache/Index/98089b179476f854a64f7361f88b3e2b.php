<div id="section" class="clearfix">
	<article style="line-height:2em;padding: 30px;">
		<h2 style="font-weight:bold; font-size:25px; text-align: center;">关于开展电子科技大学首届优秀博客、博文评选的通知</h2>
		<p><b>校内各单位：</b></p>
		<p style="text-indent: 2em;">为深入学习贯彻党的十八大和十八届三中、四中全会精神，积极培育和践行社会主义核心价值观，按照教育部、国信办《关于进一步加强高等学校网络建设和管理工作的意见》要求，弘扬网上思想文化主旋律，推进网络思想文化阵地建设，增强网络文化育人功效，经研究，决定开展电子科技大学首届优秀博客、博文评选活动。现将有关事项通知如下：</p>
		<p style="text-indent: 2em;"><b>一、活动主题</b></p>
		<p style="text-indent: 2em;">围绕“培育和践行社会主义核心价值观”这一要求，以师生博客博文为载体，推进高校网络思想文化影响力建设，丰富高校网络精神文化生活，营造奋发健康向上的校园网络文化氛围。</p>
		<p style="text-indent: 2em;"><b>二、组织机构</b></p>
		<p style="text-indent: 2em;">指导机构：学校网络文化建设领导小组</p>
		<p style="text-indent: 2em;">主办单位：学校网络文化建设工作办公室</p>
		<p style="text-indent: 2em;">媒体支持：“面聊”APP、“成电微盟”官方微博微信</p>
		<p style="text-indent: 2em;"><b>三、奖项设置</b></p>
		<p style="text-indent: 2em;">最佳博客10个，奖金2000元；</p>
		<p style="text-indent: 2em;">最佳博文10篇，奖金1000元；</p>
		<p style="text-indent: 2em;">优秀博文20篇，奖金500元；</p>
		<p style="text-indent: 2em;">优秀组织奖5个，奖金500元。</p>
		<p style="text-indent: 2em;"><b>四、参评对象</b></p>
		<p style="text-indent: 2em;"><b>参评对象为我校各机关职能部门、各学院的教职工、学生等，其中参评博客</b>应是我校教职工、学生在新浪、腾讯、大学生在线、科学网等各大网站上开通的工作博客、微博、个人主页等。博客可以是师生个人博客，也可以是团队博客。</p>
		<p style="text-indent: 2em;"><b>参评博文</b>应是我校教职工、学生在网络平台上撰写的有关于爱国教育、理想信念、文化传承、热点评论、行业发展、科研体会、教学心得、成才励志、生活感悟等相关方面的原创性文章（新闻报道类稿件除外）。博文可独立成篇，也可是围绕同一主题、解决同一问题的一组博文。</p>
		<p style="text-indent: 2em;"><b>五、评选条件</b></p>
		<p style="text-indent: 2em;"><b>优秀博客评选条件：</b></p>
		<p style="text-indent: 2em;">（1）版面新颖，内容充实；</p>
		<p style="text-indent: 2em;">（2）受大学生关注，和大学生有一定的互动交流性；</p>
		<p style="text-indent: 2em;">（3）内容积极向上，充满正能量；</p>
		<p style="text-indent: 2em;">（4）特色鲜明、主题突出，具有一定的可读性、导向性、研讨性；</p>
		<p style="text-indent: 2em;">（5）更新频率高、原创性文章在50%以上，访问量大。</p>
		<p style="text-indent: 2em;"><b>优秀博文评选条件：</b></p>
		<p style="text-indent: 2em;">（1）博文应是参评者2014年度在本人博客上发表的原创作品； </p>
		<p style="text-indent: 2em;">（2）博文紧扣主题，积极向上，提倡文以载道，真情实感，传播正能量，能体现新时期师生良好的精神风貌和精神生活； </p>
		<p style="text-indent: 2em;">（3）博文鼓励辅以图片、音视频等资料，以增强作品感染力；</p>
		<p style="text-indent: 2em;">（4）每篇文章的字数不得少于400字（文学作品字数不限）。</p>
		<p style="text-indent: 2em;"><b>优秀组织单位评选条件：</b></p>
		<p style="text-indent: 2em;">（1）积极动员和组织本单位师生员工参与优秀博文博客评选；</p>
		<p style="text-indent: 2em;">（2）制定院级优秀博文、博客评选方案；</p>
		<p style="text-indent: 2em;">（3）及时总结上报相关材料。</p>
		<p style="text-indent: 2em;"><b>六、 评选步骤</b></p>
		<p style="text-indent: 2em">学院初选（2014年11月30日-2015年1月）：个人或集体自主推荐博客或博文报所在职能部门和学院，由各单位组织评委进行初选，并从初选作品中选送3-5个/篇候选博客、博文上报学校网络文化建设工作办公室。</p>
		<p style="text-indent: 2em">网上投票（2015年3月13日-3月20日）：学校网络文化建设工作办公室汇总候选博客、博文，在专门的网页上链接展示，由师生投票产生正式候选博客20个、候选博文50篇。</p>
		<p style="text-indent: 2em">专家评审（2015年3月21-2015年4月4日）：学校网络文化建设工作办公室组织专家对正式候选博客、博文进行评审，产生10个最佳博客和10篇最佳博文、20篇优秀博文。</p>
		<p style="text-indent: 2em">宣传推广（2015年4月6日-2015年4月19日）：学校网络文化建设工作办公室在全校范围对获奖的博客、博文进行表彰、宣传、推广、交流。</p>
		<p style="text-indent: 2em;text-align:right"><b>电子科技大学网络文化建设工作办公室</b></p>
		<p style="text-indent: 2em;text-align:right"><b>2014年11月</b></p>
